using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Class_Exercise
{
    class Calculator
    {

        public int AddTwenty(int num)
        {
            return num + 20;
        }

        public int MinusTwenty(int num)
        {
            return num - 20;
        }

        public int TimesTwenty(int num)
        {
            return num * 20;
        }
    }
}
